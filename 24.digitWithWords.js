function digit(number) {
    switch (number) {
        case 'zero':
            console.log(0);
            break;
        case 'one':
            console.log(1);
            break;
        case 'two':
            console.log(2);
            break;
        case 'three':
            console.log(3);
            break;
        case 'four':
            console.log(4);
            break;
        case 'five':
            console.log(5);
            break;

    }
}
digit('four')